	CREATE TABLE Cliente (
	Id_Cliente UNIQUEIDENTIFIER PRIMARY KEY,
	Cedula VARCHAR (16),
	NombresCl VARCHAR (70),
	ApellidosCl VARCHAR (70),
	Telefono varchar(50),
	Direccion VARCHAR (200),
	Sexo CHAR (1) DEFAULT ('F'),
	CONSTRAINT CH_Cliente CHECK((UPPER(Sexo)= 'F' OR UPPER (Sexo)='M')),
	CONSTRAINT UQ_Cedula UNIQUE (Cedula));

	CREATE TABLE Pedido (
	Id_Pedido UNIQUEIDENTIFIER NOT NULL,
	Fecha DATETIME,
	Id_Cliente UNIQUEIDENTIFIER NOT NULL,
	CONSTRAINT PK_PedidoCliente PRIMARY KEY NONCLUSTERED (Id_Pedido, Id_Cliente),
	CONSTRAINT CH_Fecha CHECK (Fecha<=GETDATE())
	);

	CREATE TABLE Proveedor (
	Id_proveedor UNIQUEIDENTIFIER PRIMARY KEY,
	Nombres VARCHAR (150) NOT NULL,
	Telefono INT NOT NULL,
	SitioWeb VARCHAR (250) NOT NULL
	);

	CREATE TABLE Articulo (
	Id_Articulo UNIQUEIDENTIFIER NOT NULL,
	NombreArt VARCHAR (120),
	Precio NUMERIC (5,2),
	Stock INT,
	Id_Proveedor UNIQUEIDENTIFIER NOT NULL,
	CONSTRAINT CH_Precio CHECK (Precio>0),
	CONSTRAINT PK_ArticuloProveedor PRIMARY KEY NONCLUSTERED (Id_Articulo, Id_Proveedor),
	);

	CREATE TABLE DetallePedido (
	Cant INT,
	Precio NUMERIC (5,2),
	Descuento NUMERIC (5,2),
	MontoT NUMERIC (5,2),
	MontoF NUMERIC (5,2),
	Id_Articulo UNIQUEIDENTIFIER NOT NULL,
	Id_Pedido UNIQUEIDENTIFIER NOT NULL,
	CONSTRAINT PK_ArticuloPedido PRIMARY KEY NONCLUSTERED (Id_Articulo, Id_Pedido),
	);


